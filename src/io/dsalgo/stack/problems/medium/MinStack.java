package io.dsalgo.stack.problems.medium;

import java.util.Stack;

// leetcode 155. Min Stack
class MinStack {
    Stack<Integer> stack;
    Stack<Integer> minStack;

    public MinStack() {
        stack = new Stack<>();
        minStack = new Stack<>();
    }
    
    public void push(int val) {
        stack.push(val);
        if(minStack.empty() || val <= minStack.peek()){
            minStack.push(val);
        }
    }
    
    public void pop() {
        int pooped = stack.pop();
        if(pooped == minStack.peek()){
            minStack.pop();
        }
    }
    
    public int top() {
        return stack.peek();
    }
    
    public int getMin() {
        return minStack.peek();
    }
}

